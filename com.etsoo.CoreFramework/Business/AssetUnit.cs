namespace com.etsoo.CoreFramework.Business
{
    /// <summary>
    /// Asset units
    /// 资产单位
    /// </summary>
    public enum AssetUnit : byte
    {
        /// <summary>
        /// Hour
        /// 小时
        /// </summary>
        HOUR = 10,

        /// <summary>
        /// Day
        /// 天
        /// </summary>
        DAY = 11,

        /// <summary>
        /// Year
        /// 年
        /// </summary>
        YEAR = 12,

        /// <summary>
        /// Week
        /// 周
        /// </summary>
        WEEK = 21,

        /// <summary>
        /// Two weeks
        /// 两周
        /// </summary>
        FORTNIGHT = 22,

        /// <summary>
        /// Four weeks
        /// 四周
        /// </summary>
        FOURWEEK = 24,

        /// <summary>
        /// Month
        /// 月
        /// </summary>
        MONTH = 31,

        /// <summary>
        /// Two months
        /// 两月
        /// </summary>
        BIMONTH = 32,

        /// <summary>
        /// Quater
        /// 季度
        /// </summary>
        QUATER = 33,

        /// <summary>
        /// Half year
        /// 半年
        /// </summary>
        HALFYEAR = 36,

        /// <summary>
        /// Time
        /// 次
        /// </summary>
        TIME = 99
    }
}
