namespace com.etsoo.Localization.Chinese
{
    /// <summary>
    /// Character tone
    /// 汉字声调
    /// </summary>
    public enum CharTone : byte
    {
        /// <summary>
        /// First tone
        /// 一声，阴平
        /// </summary>
        First = 1,

        /// <summary>
        /// Second tone
        /// 二声，阳平
        /// </summary>
        Second = 2,

        /// <summary>
        /// Third tone
        /// 三声，上声
        /// </summary>
        Third = 3,

        /// <summary>
        /// Fourth tone
        /// 四声，去声
        /// </summary>
        Fourth = 4
    }
}
